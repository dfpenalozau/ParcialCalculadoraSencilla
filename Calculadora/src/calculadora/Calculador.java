package calculadora;

public class Calculador {

    public float Numero1;
    public float Numero2;
    public float Resultado;
    
    public float Sumar(){
        
        Resultado=Numero1+Numero2;
        return Resultado;
    
    }
    
       public float Restar(){
        
        Resultado=Numero1-Numero2;
        return Resultado;
    
    }
    public float Multiplicar(){
        
        Resultado=Numero1*Numero2;
        return Resultado;
    
    }
    
       public float Dividir(){
        
        Resultado=Numero1/Numero2;
        return Resultado;
    
    }
}

       



   
